/*
 * Straightforwardly output the Beaufort wind speed scale.
 */
class Beaufort {
	public static void main(String[] args) {
		System.out.println("Force 0: Calm. Smoke rises vertically.");
		System.out.println("Force 1: Smoke drift indicates wind direction. Leaves and wind vanes are stationary.");
		System.out.println("Force 2: Wind felt on exposed skin. Leaves rustle. Wind vanes begin to move.");
		System.out.println("Force 3: Leaves and small twigs constantly moving, light flags extended.");
		System.out.println("Force 4: Dust and loose paper raised. Small branches begin to move.");
		System.out.println("Force 5: Branches of a moderate size move. Small trees in leaf begin to sway.");
		System.out.println("Force 6: Large branches in motion. Whistling heard in overhead wires. Umbrella use becomes difficult. Empty plastic bins tip over.");
		System.out.println("Force 7: Whole trees in motion. Effort needed to walk against the wind.");
		System.out.println("Force 8: Some twigs broken from trees. Cars veer on road. Progress on foot is seriously impeded.");
		System.out.println("Force 9: Some branches break off trees, and some small trees blow over. Construction/temporary signs and barricades blow over.");
		System.out.println("Force 10: Trees are broken off or uprooted, structural damage likely");
		System.out.println("Force 11: Widespread vegetation and structural damage likely.");
		System.out.println("Force 12: Severe widespread damage to vegetation and structures. Debris and unsecured objects are hurled about.");
	}
}